# -*- coding: utf-8 -*-
"""
ステップ1〜3: GTFSデータリポジトリの /files APIから全国のフィード一覧を取得し、
都道府県別に集計 → 山形県分を抽出してCSVに保存するスクリプト。

実行方法:  python3 step1_feeds_list.py
"""

import requests   # HTTP通信(APIを呼び出す)ためのライブラリ
import pandas as pd  # 表形式データを扱うライブラリ

# ---------------------------------------------------------------
# ステップ1: /files エンドポイントから全フィードのメタデータを取得
# ---------------------------------------------------------------
API_URL = "https://api.gtfs-data.jp/v2/files"

# requests.get() でAPIにアクセスし、.json() でJSON(辞書型)に変換する
response = requests.get(API_URL, timeout=60)
response.raise_for_status()  # 通信エラー(404や500など)があればここで例外を出して止まる
data = response.json()

# このAPIは {"code": 200, "message": "ok", "body": [フィードのリスト]} という形で返ってくる
feeds = data["body"]
print(f"取得したフィード数: {len(feeds)} 件")

# ---------------------------------------------------------------
# ステップ2: pandasのDataFrameにして、都道府県別のフィード数を集計
# ---------------------------------------------------------------
# 辞書のリストをそのまま渡すと、キーが列名になった表(DataFrame)ができる
df = pd.DataFrame(feeds)

# feed_pref_id はJIS都道府県コード(1=北海道 … 47=沖縄)。
# 数字のままだと分かりにくいので、名前に変換する対応表を用意する。
PREF_NAMES = {
    1: "北海道", 2: "青森県", 3: "岩手県", 4: "宮城県", 5: "秋田県",
    6: "山形県", 7: "福島県", 8: "茨城県", 9: "栃木県", 10: "群馬県",
    11: "埼玉県", 12: "千葉県", 13: "東京都", 14: "神奈川県", 15: "新潟県",
    16: "富山県", 17: "石川県", 18: "福井県", 19: "山梨県", 20: "長野県",
    21: "岐阜県", 22: "静岡県", 23: "愛知県", 24: "三重県", 25: "滋賀県",
    26: "京都府", 27: "大阪府", 28: "兵庫県", 29: "奈良県", 30: "和歌山県",
    31: "鳥取県", 32: "島根県", 33: "岡山県", 34: "広島県", 35: "山口県",
    36: "徳島県", 37: "香川県", 38: "愛媛県", 39: "高知県", 40: "福岡県",
    41: "佐賀県", 42: "長崎県", 43: "熊本県", 44: "大分県", 45: "沖縄県",
    46: "鹿児島県", 47: "沖縄県",
}
# ↑ 注意: 45=宮崎県, 46=鹿児島県, 47=沖縄県 が正しいJISコード
PREF_NAMES[45] = "宮崎県"

# .map() で「コード → 名前」の変換を列全体に一括で適用し、新しい列を作る
df["都道府県"] = df["feed_pref_id"].map(PREF_NAMES)

# value_counts() は「値ごとの出現回数」を数える関数。都道府県別のフィード数になる
pref_counts = df["都道府県"].value_counts()
print("\n=== 都道府県別フィード数(上位20) ===")
print(pref_counts.head(20))

# ---------------------------------------------------------------
# ステップ3: 山形県のフィードだけ抽出してCSVに保存
# ---------------------------------------------------------------
# df[条件式] で条件に合う行だけを取り出せる(これを「フィルタリング」と言う)
yamagata = df[df["feed_pref_id"] == 6].copy()

# 見たい列だけを選んで、列名を日本語に付け替える
cols = {
    "organization_name": "事業者名",
    "feed_name": "フィード名",
    "file_last_updated_at": "最終更新日",
    "file_from_date": "データ開始日",
    "file_to_date": "データ有効期限",
    "feed_license_id": "ライセンス",
    "file_url": "ダウンロードURL",
}
yamagata_view = yamagata[list(cols.keys())].rename(columns=cols)

# 最終更新日は「2025-09-22T14:01:07.346408+09:00」のように長いので、日付部分だけにする
yamagata_view["最終更新日"] = yamagata_view["最終更新日"].str[:10]

print(f"\n=== 山形県のフィード一覧({len(yamagata_view)}件) ===")
# to_string() を使うと、列が省略されずに全部表示される
print(yamagata_view[["事業者名", "フィード名", "最終更新日", "データ有効期限"]].to_string(index=False))

# CSVに保存。encoding="utf-8-sig" にするとExcelで開いても文字化けしない
yamagata_view.to_csv("yamagata_gtfs_feeds.csv", index=False, encoding="utf-8-sig")
print("\n→ yamagata_gtfs_feeds.csv に保存しました")
